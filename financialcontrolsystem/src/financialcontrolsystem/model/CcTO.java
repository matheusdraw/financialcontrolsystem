package financialcontrolsystem.model;

public class CcTO {

	private int id;
	private int idcontas;
	private int cc;
	private int ag;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getIdcontas() {
		return idcontas;
	}

	public void setIdcontas(int idcontas) {
		this.idcontas = idcontas;
	}

	public int getCc() {
		return cc;
	}

	public void setCc(int cc) {
		this.cc = cc;
	}

	public int getAg() {
		return ag;
	}

	public void setAg(int ag) {
		this.ag = ag;
	}
}
